// 格式化函數，支援 %s|%d|%v 等佔位符
export const t = (text: string, ...args: any[]): string => {
  let translated = text;
  if (currentLanguage === 'en-US') {
    if (enTranslations[text] !== undefined) {
      translated = enTranslations[text];
    }
  }

  if (args.length > 0) {
    args.forEach(arg => {
      translated = translated.replace(/%s|%d|%v/, String(arg));
    });
  }
  return translated;
};
